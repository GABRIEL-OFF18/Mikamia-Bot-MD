const handler = async (msg, { conn, args, usedPrefix, command }) => {
  const tipo = args[0] ? args[0].toLowerCase() : 'random'

  let arte
  let categoria = ''

  if (tipo === 'pene' || tipo === 'dick' || tipo === 'pija') {
    arte = diseños.penes[Math.floor(Math.random() * diseños.penes.length)]
    categoria = '🍆 **Diseño de Pene**'
  } 
  else if (tipo === 'chica' || tipo === 'waifu' || tipo === 'anime' || tipo === 'girl') {
    arte = diseños.chicas[Math.floor(Math.random() * diseños.chicas.length)]
    categoria = '💕 **Chica Anime / Waifu**'
  } 
  else {
    // Random total
    const todo = [...diseños.penes, ...diseños.chicas]
    arte = todo[Math.floor(Math.random() * todo.length)]
    categoria = '🎲 **Diseño Random**'
  }

  const texto = `${categoria}\n\n` +
                `${arte}\n\n` +
                `Comandos disponibles:\n` +
                `• \( {usedPrefix} \){command} → random\n` +
                `• \( {usedPrefix} \){command} pene\n` +
                `• \( {usedPrefix} \){command} chica\n` +
                `• \( {usedPrefix} \){command} waifu`

  await conn.sendMessage(msg.chat, { text: texto }, { quoted: msg })
}

handler.help = ['diseño', 'diseñopene', 'waifu', 'animeart']
handler.tags = ['fun']
handler.command = ['diseño', 'diseñopene', 'dickart', 'waifu', 'animeart', 'chica']

export default handler